				    <h5>introducción</h5>
					
						<p>Utelpa y Ctera formamos parte del Movimiento Pedagógico Latinoamericano,
						  movimiento que está en marcha desde el año 2011. Somos conscientes de la
						  heterogeneidad de nuestros países y de nuestros sistemas educativos, unidos
						  bajo un objetivo común, en “pensar y proponer políticas para la educación pública”
						  que requieren nuestros pueblos para avanzar en la transformación e integración
						  Latinoamericana, es decir, una educación emancipadora e igualitaria, como lo
						  entendía Paulo Freire, transformar a las personas para que ellas transformen la
						  realidad. Por eso, nuevamente, en el 2015 UTELPa realizará un Congreso, donde
						  los trabajadores y trabajadoras de la educación tomarán la palabra para recuperar
						  el pensamiento pedagógico que produce el trabajo docente.</p>
						<p>Será una instancia donde compartiremos diferentes modos de pensar nuestra
						  construcción de la pedagogía desde las aulas, que son el fundamento de una
						  educación pública, popular y democrática latinoamericana, y que aportan al
						  fortalecimiento de nuestra soberanía intelectual, política y pedagógica.					    </p>
						<p>La escuela es el agente transformador, se constituye en un espacio de
						  construcción de saberes populares e instrumento de lucha contra hegemónica. La
						  escuela construye encuentros entre iguales sobre realidades diversas en un
						  contexto específico donde se desarrollan sujetos históricos concretos, con una
						  lógica de vivir solidaria y de aprender colectivamente.					    </p>
						<p>Las organizaciones debemos vincular cada vez más nuestros temas de debate y
						  de acción a la pregunta por la nueva pedagogía que queremos construir, a la
						  nueva pedagogía desde la que queremos actuar para profundizar las
						  transformaciones sociales, políticas y culturales de nuestros países.					    </p>
						<p>Este encuentro nos interpela a los Sindicatos de la Educación como interlocutores
						  sociales. Tenemos la tarea de desarrollar capacidad (constante y sistemática) de
						  reflexión y de elaboración de propuestas educativas que contemplen la globalidad
						  de la realidad social, económica, política y cultural, sin dejar de lado el
					    compromiso de representación y de movilización de nuestro sector.</p>
